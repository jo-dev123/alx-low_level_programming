/**
 * add - return addtion
 * @a: first operand
 * @b: second operand
 * Return: result
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtraction
 * @a: first operand
 * @b: second operand
 * Return: result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - division
 * @a: first operand
 * @b: second operand
 * Return: result
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mul - multiflication
 * @a: first operand
 * @b: second operand
 * Return: result
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * mod - modulo
 * @a: first operand
 * @b: second operand
 * Return: result
 */

int mod(int a, int b)
{
	return (a % b);
}
